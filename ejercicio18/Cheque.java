package ejercicio18;
public class Cheque {
 private double montoCheque;
 private int numCheque;

    /**
     * @return the montoCheque
     */
    public double getMontoCheque() {
        return montoCheque;
    }

    /**
     * @param montoCheque the montoCheque to set
     */
    public void setMontoCheque(double montoCheque) {
        this.montoCheque = montoCheque;
    }

    /**
     * @return the numCheque
     */
    public int getNumCheque() {
        return numCheque;
    }

    /**
     * @param numCheque the numCheque to set
     */
    public void setNumCheque(int numCheque) {
        this.numCheque = numCheque;
    }
}
