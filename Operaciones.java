package practicafunciones2;
public class Operaciones {
    
public int suma(int n1, int n2){
    int resultado = n1 + n2;
System.out.println(resultado);
    return resultado;
}
public int resta(int n1, int n2){
    int resultado = n1 - n2;
System.out.println(resultado);
    return resultado;
}
public int multiplica(int n1, int n2){
    int resultado = n1 * n2;
System.out.println(resultado);
    return resultado;
}
    
}
