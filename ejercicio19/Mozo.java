package ejercicio19;
public class Mozo {
private int numeroMozo;
private double comision;
private double montoFacturado;

    /**
     * @return the numeroMozo
     */
    public int getNumeroMozo() {
        return numeroMozo;
    }

    /**
     * @param numeroMozo the numeroMozo to set
     */
    public void setNumeroMozo(int numeroMozo) {
        this.numeroMozo = numeroMozo;
    }

    /**
     * @return the comision
     */
    public double getComision() {
        return comision;
    }

    /**
     * @param comision the comision to set
     */
    public void setComision(double comision) {
        this.comision = comision;
    }

    /**
     * @return the montoFacturado
     */
    public double getMontoFacturado() {
        return montoFacturado;
    }

    /**
     * @param montoFacturado the montoFacturado to set
     */
    public void setMontoFacturado(double montoFacturado) {
        this.montoFacturado = montoFacturado;
    }
}
